%% gradient 2 + well Moelans formulation
function [dphia, dphib] = calcPhi_grad2_moelans(phia,phib,phic,laplace_a,laplace_b,params)
    zero = 1e-15;
    nx = params.nx;
    ny = params.ny;
    dx = params.dx;
    dy = params.dy;
    kab = params.kab;
    kac = params.kac;
    kbc = params.kbc;
    Oab = params.Oab;
    Oac = params.Oac;
    Obc = params.Obc;
    
    i=2:nx-1;
    j=2:ny-1;

    sumSquares = phia(i,j).^2.*phib(i,j).^2 + phia(i,j).^2.*phic(i,j).^2 + phib(i,j).^2.*phic(i,j).^2;
    [row,col] = find(sumSquares<=zero);
    
    bulk = row+(nx-2)*(col-1);
    sumSquares(bulk)=1.0;
    Ozero = (Oab.*phia(i,j).^2.*phib(i,j).^2 + Oac.*phia(i,j).^2.*phic(i,j).^2 + Obc.*phib(i,j).^2.*phic(i,j).^2)./sumSquares;
    
    dF_dphia = 2.*kab.*(  2.*phia(i,j).*((phib(i+1,j)-phib(i-1,j)).^2/(4*dx*dx) + (phib(i,j+1)-phib(i,j-1)).^2/(4*dy*dy)) ...
                         - 2.*phib(i,j).*((phia(i+1,j)-phia(i-1,j)).*(phib(i+1,j)-phib(i-1,j))/(4*dx*dx) + (phia(i,j+1)-phia(i,j-1)).*(phib(i,j+1)-phib(i,j-1))/(4*dy*dy)) ...
                         + phia(i,j).*phib(i,j).*laplace_b(i,j) - phib(i,j).^2.*laplace_a(i,j))...
              + 2.*kac.*(  2.*phia(i,j).*((phic(i+1,j)-phic(i-1,j)).^2/(4*dx*dx) + (phic(i,j+1)-phic(i,j-1)).^2/(4*dy*dy)) ...
                         - 2.*phic(i,j).*((phia(i+1,j)-phia(i-1,j)).*(phic(i+1,j)-phic(i-1,j))/(4*dx*dx) + (phia(i,j+1)-phia(i,j-1)).*(phic(i,j+1)-phic(i,j-1))/(4*dy*dy)) ...
                         - phia(i,j).*phic(i,j).*(laplace_a(i,j)+laplace_b(i,j)) - phic(i,j).^2.*laplace_a(i,j))...
               + 1.5.*Oab.*phia(i,j).*phib(i,j).^2 + 1.5.*Oac.*phia(i,j).*phic(i,j).^2 + 0.5.*Ozero.*(phia(i,j).^3 - phia(i,j))...
               + phia(i,j).*((Oab-Ozero).*phib(i,j).^2+(Oac-Ozero).*phic(i,j).^2)./sumSquares.*((phia(i,j).^4 + phib(i,j).^4 + phic(i,j).^4)./4 - (phia(i,j).^2 +phib(i,j).^2 +phic(i,j).^2)./2 + 1/4);
    dF_dphib = 2.*kab.*(  2.*phib(i,j).*((phia(i+1,j)-phia(i-1,j)).^2/(4*dx*dx) + (phia(i,j+1)-phia(i,j-1)).^2/(4*dy*dy)) ...
                         - 2.*phia(i,j).*((phia(i+1,j)-phia(i-1,j)).*(phib(i+1,j)-phib(i-1,j))/(4*dx*dx) + (phia(i,j+1)-phia(i,j-1)).*(phib(i,j+1)-phib(i,j-1))/(4*dy*dy)) ...
                         + phia(i,j).*phib(i,j).*laplace_a(i,j) - phia(i,j).^2.*laplace_b(i,j))...
              + 2.*kbc.*(  2.*phib(i,j).*((phic(i+1,j)-phic(i-1,j)).^2/(4*dx*dx) + (phic(i,j+1)-phic(i,j-1)).^2/(4*dy*dy)) ...
                         - 2.*phic(i,j).*((phib(i+1,j)-phib(i-1,j)).*(phic(i+1,j)-phic(i-1,j))/(4*dx*dx) + (phib(i,j+1)-phib(i,j-1)).*(phic(i,j+1)-phic(i,j-1))/(4*dy*dy)) ...
                         - phib(i,j).*phic(i,j).*(laplace_a(i,j)+laplace_b(i,j)) - phic(i,j).^2.*laplace_b(i,j))...
               + 1.5.*Oab.*phib(i,j).*phia(i,j).^2 + 1.5.*Obc.*phib(i,j).*phic(i,j).^2 + 0.5.*Ozero.*(phib(i,j).^3 - phib(i,j))...
               + phib(i,j).*((Oab-Ozero).*phia(i,j).^2+(Obc-Ozero).*phic(i,j).^2)./sumSquares.*((phia(i,j).^4 + phib(i,j).^4 + phic(i,j).^4)./4 - (phia(i,j).^2 +phib(i,j).^2 +phic(i,j).^2)./2 + 1/4);
    dF_dphic = 2.*kac.*(  2.*phic(i,j).*((phia(i+1,j)-phia(i-1,j)).^2/(4*dx*dx) + (phia(i,j+1)-phia(i,j-1)).^2/(4*dy*dy)) ...
                          -2.*phia(i,j).*((phia(i+1,j)-phia(i-1,j)).*(phic(i+1,j)-phic(i-1,j))/(4*dx*dx) + (phia(i,j+1)-phia(i,j-1)).*(phic(i,j+1)-phic(i,j-1))/(4*dy*dy)) ...
                          + phia(i,j).*phic(i,j).*laplace_a(i,j) + phia(i,j).^2.*(laplace_a(i,j)+laplace_b(i,j)))...
              + 2.*kbc.*(  2.*phic(i,j).*((phib(i+1,j)-phib(i-1,j)).^2/(4*dx*dx) + (phib(i,j+1)-phib(i,j-1)).^2/(4*dy*dy)) ...
                         - 2.*phib(i,j).*((phib(i+1,j)-phib(i-1,j)).*(phic(i+1,j)-phic(i-1,j))/(4*dx*dx) + (phib(i,j+1)-phib(i,j-1)).*(phic(i,j+1)-phic(i,j-1))/(4*dy*dy)) ...
                         + phib(i,j).*phic(i,j).*laplace_b(i,j) + phib(i,j).^2.*(laplace_a(i,j)+laplace_b(i,j)))...
               + 1.5.*Oac.*phic(i,j).*phia(i,j).^2 + 1.5.*Obc.*phic(i,j).*phib(i,j).^2 + 0.5.*Ozero.*(phic(i,j).^3 - phic(i,j))...
               + phic(i,j).*((Oac-Ozero).*phia(i,j).^2+(Obc-Ozero).*phib(i,j).^2)./sumSquares.*((phia(i,j).^4 + phib(i,j).^4 + phic(i,j).^4)./4 - (phia(i,j).^2 +phib(i,j).^2 +phic(i,j).^2)./2 + 1/4);
           
    dphia = 2.*dF_dphia - dF_dphib - dF_dphic;
    dphib = 2.*dF_dphib - dF_dphia - dF_dphic;
end